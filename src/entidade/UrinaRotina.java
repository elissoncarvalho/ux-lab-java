
package entidade;

public class UrinaRotina {
    private int codigoUR;
    private String cor;
    private String densidade;
    private String aspecto;
    private double ph;
    private String celEpiteliais;
    private String leucocitos;
    private String hemacias;
    private String cilindros;

    public int getCodigoUR() {
        return codigoUR;
    }

    public void setCodigoUR(int codigoUR) {
        this.codigoUR = codigoUR;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getDensidade() {
        return densidade;
    }

    public void setDensidade(String densidade) {
        this.densidade = densidade;
    }

    public String getAspecto() {
        return aspecto;
    }

    public void setAspecto(String aspecto) {
        this.aspecto = aspecto;
    }

    public double getPh() {
        return ph;
    }

    public void setPh(double ph) {
        this.ph = ph;
    }

    public String getCelEpiteliais() {
        return celEpiteliais;
    }

    public void setCelEpiteliais(String celEpiteliais) {
        this.celEpiteliais = celEpiteliais;
    }

    public String getLeucocitos() {
        return leucocitos;
    }

    public void setLeucocitos(String leucocitos) {
        this.leucocitos = leucocitos;
    }

    public String getHemacias() {
        return hemacias;
    }

    public void setHemacias(String hemacias) {
        this.hemacias = hemacias;
    }

    public String getCilindros() {
        return cilindros;
    }

    public void setCilindros(String cilindros) {
        this.cilindros = cilindros;
    }
    
}
